%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
%
% This script uses the nomenclature, formulations and solutions from:
%   M. Avillez and D. Arnas, "Osculating and Mean Asynchronous Relative Motion Approximations 
%   Under J2 and Atmospheric Drag", TODO
%
% Summary:
%   Given the initial relative state and the initial state of the chief, computes the
%     osculating relative state at the selected argument of latitude.
%   Implements the solution from section IV.B of the paper.
%
% Inputs:
%   tt: argument of latitude at which to compute the osculating relative state
%   mu: gravitational parameter
%   j2: J2 coefficient of the gravity model
%   R: Radius of the central planet
%   tti: Initial argument of latitude
%   relativeStateInitial: Initial relative state -> see Note 1
%   stateInitialC: Initial state of the chief -> see Note 1
%   kD: Ballistic parameter of the deputy (D)
%   rhoD: Atmospheric density acting on the deputy (D)
%   kC: Ballistic parameter of the chief (C)
%   rhoC: Atmospheric density acting on the chief (C)
%   expansionOrder: Order of the expansion to use (2 or 3)
%
% Outputs:
%   relativeState: Osculating relative state at the selected argument of latitude. -> see Note 1
%
% Note 1:
%   Relative states: [dbb; dx; dy; dp; doo; dt]
%   Absolute states: [bb; x; y; p; oo; t]
%
% Authors: Miguel Avillez and David Arnas
% Modified: August 2025
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function relativeState = osculatingRelativeState(tt, mu, j2, R, tti, relativeStateInitial, stateInitialC, ...
  kD, rhoD, kC, rhoC, expansionOrder)

  if expansionOrder == 2
    relativeState = osculatingRelativeStateOrder2(tt, mu, j2, R, tti, relativeStateInitial, stateInitialC, kD, rhoD, kC, rhoC);
  elseif expansionOrder == 3
    relativeState = osculatingRelativeStateOrder3(tt, mu, j2, R, tti, relativeStateInitial, stateInitialC, kD, rhoD, kC, rhoC);
  else
    error("Invalid expansion order")
  end

end

