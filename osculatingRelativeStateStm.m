%
% This script uses the nomenclature, formulations and solutions from:
%   M. Avillez and D. Arnas, "Osculating and Mean Asynchronous Relative Motion Approximations 
%   Under J2 and Atmospheric Drag", TODO
%
% Summary:
%   Given the initial state of the chief, computes the osculating state transition matrix (STM) 
%     and sensitivity matrix.
%   Implements the solution from section VI of the paper.
%
% Inputs:
%   tt: argument of latitude at which to compute the STM and sensitivity matrix
%   mu: gravitational parameter
%   j2: J2 coefficient of the gravity model
%   R: Radius of the central planet
%   tti: Initial argument of latitude
%   stateInitialC: Initial state of the chief -> see Note 1
%   kC: Ballistic parameter of the chief (C)
%   rhoC: Atmospheric density acting on the chief (C)s
%   expansionOrder: Order of the expansion to use (2 or 3)
%
% Outputs:
%   phi: Osculating-elements state transition matrix at the selected argument of latitude.
%   s: Osculating-elements sensitivity matrix at the selected argument of latitude.
%
% Note 1:
%   Absolute states: [bb; x; y; p; oo; t]
%
% Authors: Miguel Avillez and David Arnas
% Modified: August 2025
%
%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
function [phi, s] = osculatingRelativeStateStm(tt, mu, j2, R, tti, stateInitialC, kC, rhoC, expansionOrder)

  if expansionOrder == 2
    [phi, s] = osculatingRelativeStateOrderStm2(tt, mu, j2, R, tti, stateInitialC, kC, rhoC);
  elseif expansionOrder == 3
    [phi, s] = osculatingRelativeStateOrderStm3(tt, mu, j2, R, tti, stateInitialC, kC, rhoC);
  else
    error("Invalid expansion order")
  end

end

